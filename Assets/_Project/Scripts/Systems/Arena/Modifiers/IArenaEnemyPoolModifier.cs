using System.Collections.Generic;

// IArenaEnemyPoolModifier
// Это интерфейс для ВСЕХ будущих модификаторов пула врагов.
//
// ВАЖНО:
// Этот модификатор отвечает ТОЛЬКО за пул врагов.
//
// Он может:
// - убрать врага из пула;
// - оставить врага в пуле;
// - повысить/понизить вес выбора;
// - временно усилить приоритет определённого типа врага.
//
// Он НЕ отвечает за:
// - бюджет волны;
// - бафы/дебафы;
// - урон/защиту;
// - силу заклинаний;
// - боевые статы;
// - фракционные бонусы к магии.
//
// Для этого потом будут отдельные слои.
public interface IArenaEnemyPoolModifier
{
    // runContext = текущее состояние забега
    // waveNumber = номер волны
    // candidates = текущий список кандидатов, который модификатор может менять
    void ModifyPool(
        ArenaRunContext runContext,
        int waveNumber,
        List<ArenaEnemyPoolCandidate> candidates
    );
}