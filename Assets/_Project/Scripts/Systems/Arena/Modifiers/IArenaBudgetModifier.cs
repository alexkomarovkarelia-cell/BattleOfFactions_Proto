// ВАЖНО:
// Этот модификатор отвечает ТОЛЬКО за бюджет волны.
//
// Он может:
// - увеличить бюджет
// - уменьшить бюджет
// - добавить фиксированную поправку
// - применить коэффициент к бюджету
//
// Он НЕ отвечает за:
// - бафы/дебафы игрока
// - бонусы фракций
// - силу заклинаний
// - урон/защиту/статы
// - боевые эффекты
//
// Для этого позже будет отдельный слой боевых / фракционных модификаторов.
public interface IArenaBudgetModifier
{
    // runContext     = текущее состояние забега
    // waveNumber     = номер волны
    // currentBudget  = бюджет, который уже посчитан до этого шага
    //
    // Возвращаем новый бюджет.
    int ModifyBudget(ArenaRunContext runContext, int waveNumber, int currentBudget);
}
