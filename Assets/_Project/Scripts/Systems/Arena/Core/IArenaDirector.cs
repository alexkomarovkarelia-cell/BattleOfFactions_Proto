// IArenaDirector
// Это общий "контракт" для всех директоров режимов арены.
//
// ВАЖНО:
// Мы не хотим делать один огромный ArenaDirector на все режимы.
// Поэтому у каждого режима будет свой директор:
// - ClassicArenaDirector
// - SurvivalArenaDirector
// - позже ConstructorArenaDirector
// - позже PvPArenaDirector
//
// Но при этом всем им нужен общий набор методов,
// чтобы ArenaModeController мог работать с ними одинаково.
public interface IArenaDirector
{
    // Уникальный ID режима, который обслуживает этот директор.
    // Примеры:
    // "classic"
    // "survival"
    // "constructor"
    // "pvp"
    string DirectorModeId { get; }

    // Инициализация директора перед началом забега.
    // Сюда передаётся текущий контекст забега,
    // в котором уже лежат:
    // - арена
    // - сложность
    // - режим
    // - текущее состояние забега
    void Initialize(ArenaRunContext runContext);

    // Главный метод:
    // директор анализирует текущую ситуацию
    // и пытается собрать решение для следующего шага.
    //
    // Например позже здесь директор сможет решить:
    // - какую волну запускать
    // - какой бюджет волны дать
    // - нужно ли дать раннего элитника
    // - нужно ли включить плохую видимость
    // - нужно ли изменить лут/сервисы/условия
    //
    // Пока мы просто делаем каркас.
    bool TryBuildDecision(out ArenaDirectorDecision decision);

    // Сообщаем директору, что волна завершилась.
    // Директор сможет использовать это для анализа:
    // - быстро прошли
    // - тяжело прошли
    // - слишком легко прошли
    void NotifyWaveCompleted(float waveDuration, bool wasVeryEasy);

    // Сообщаем директору, что был убит враг.
    // Потом это пригодится для статистики и реакции режима.
    void NotifyEnemyKilled();

    // Тик директора по времени.
    // Это пригодится, если директору нужно
    // постепенно отслеживать состояние забега.
    void Tick(float deltaTime);

    // Сообщаем директору, что забег завершён.
    void FinishRun();
}