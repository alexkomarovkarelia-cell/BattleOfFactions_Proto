using UnityEngine;

// Скрипт камеры: следует за целью (игроком) с фиксированным смещением.
// Очень частый паттерн в играх: Camera Follow / Third Person Camera / Top-Down Camera.
public class CameraFollow : MonoBehaviour
{
    // -----------------------------
    // НАСТРОЙКИ (Inspector)
    // -----------------------------

    // target — за кем следим (обычно Transform игрока)
    public Transform target;

    // offset — смещение камеры относительно игрока
    // Пример:
    //  (0, 10, -10) = камера выше и сзади
    //  (0, 15, 0)   = камера сверху
    public Vector3 offset;

    // followSpeed — скорость следования (насколько быстро камера догоняет цель)
    // Чем больше — тем "жёстче" и быстрее камера прилипает к позиции.
    public float followSpeed = 5f;


    // -----------------------------
    // LateUpdate — вызывается каждый кадр ПОСЛЕ Update
    // Камеру часто двигают именно здесь, чтобы:
    // - сначала персонаж обновил позицию в Update
    // - потом камера корректно "догнала" его
    // Это типовая практика в Unity.
    // -----------------------------
    void LateUpdate()
    {
        // Защита от ошибок:
        // Если target не назначен, будет NullReferenceException.
        if (target == null) return;

        // -----------------------------
        // БЛОК РАСЧЁТА ЖЕЛАЕМОЙ ПОЗИЦИИ
        // -----------------------------
        // desiredPosition — где камера должна быть "идеально":
        // позиция игрока + смещение offset
        Vector3 desiredPosition = target.position + offset;

        // -----------------------------
        // БЛОК ПЛАВНОГО ДВИЖЕНИЯ
        // -----------------------------
        // Vector3.Lerp(a, b, t) — плавный переход от a к b
        // t обычно делают через (скорость * Time.deltaTime),
        // чтобы движение не зависело от FPS.
        transform.position = Vector3.Lerp(
            transform.position,
            desiredPosition,
            followSpeed * Time.deltaTime
        );

        // В этом варианте камера только "следует".
        // Она НЕ поворачивается на игрока.
        // Если нужно — можно добавить LookAt (ниже покажу).
    }
}
